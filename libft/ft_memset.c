#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*ptrs;
	size_t	i;

	ptrs = (char *)s;
	i = 0;
	while (i < n)
	{
		ptrs[i] = c;
		i++;
	}
	return (s);
}
