#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ptrs;
	size_t	i;

	ptrs = (char *)s;
	i = 0;
	while (i < n)
	{
		ptrs[i] = 0;
		i++;
	}
}
